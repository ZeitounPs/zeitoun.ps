const getActiveLang = () => document.documentElement.lang || 'en';

const applyLanguage = (lang) => {
  const content = translations[lang] || translations.en;

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';

  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const key = node.dataset.i18n;
    node.textContent = content[key] || translations.en[key] || '';
  });

  localStorage.setItem('ztn-language', lang);
};

async function loadMessages() {
  try {
    const res = await fetch(SUPABASE_URL, {
      headers: {
        apikey: SUPABASE_KEY,
        Authorization: "Bearer " + SUPABASE_KEY
      }
    });

    const data = await res.json();

    const container = document.querySelector("#chat-list");
    if (!container) return;

    container.innerHTML = "";

    data
      .slice(-5)
      .reverse()
      .forEach((msg) => {
        const div = document.createElement("div");
        div.className = "chat-msg";

        const text = msg.text || JSON.stringify(msg);
        const MAX_LENGTH = 140;

        div.textContent =
          text.length > MAX_LENGTH ? text.slice(0, MAX_LENGTH) + "…" : text;

        container.appendChild(div);
      });
  } catch (err) {
    console.error("Supabase error:", err);
  }
}

const initApp = () => {
  const languageOptions = document.querySelectorAll('.lang-option');
  const languageToggle = document.querySelector('#language-toggle');
  const languageMenu = document.querySelector('#language-menu');
  const copyCAButton = document.querySelector('#copy-ca');
  const copyStatus = document.querySelector('#copy-status');
  const leafLayer = document.querySelector('.leaf-layer');

  const showCopyStatus = (key) => {
    if (!copyStatus) return;
    const lang = getActiveLang();
    const content = translations[lang] || translations.en;
    copyStatus.textContent = content[key] || '';
    window.setTimeout(() => {
      if (copyStatus.textContent === (content[key] || '')) {
        copyStatus.textContent = '';
      }
    }, 2200);
  };

  const closeLanguageMenu = () => {
    if (!languageMenu || !languageToggle) return;
    languageMenu.hidden = true;
    languageToggle.setAttribute('aria-expanded', 'false');
  };

  const toggleLanguageMenu = (event) => {
    if (!languageMenu || !languageToggle) return;
    if (event) event.stopPropagation();
    const willOpen = languageMenu.hidden;
    languageMenu.hidden = !willOpen;
    languageToggle.setAttribute('aria-expanded', String(willOpen));
  };

  const storedLang = localStorage.getItem('ztn-language');
  const browserLang = navigator.language?.slice(0, 2);
  const defaultLang = translations[storedLang] ? storedLang : translations[browserLang] ? browserLang : 'en';

  applyLanguage(defaultLang);
  languageOptions.forEach((button) => {
    button.classList.toggle('is-active', button.dataset.lang === defaultLang);
  });

  if (languageToggle && languageMenu) {
    languageToggle.addEventListener('click', toggleLanguageMenu);
    document.addEventListener('click', (event) => {
      if (!languageMenu.hidden && !languageMenu.contains(event.target) && !languageToggle.contains(event.target)) {
        closeLanguageMenu();
      }
    });
  }

  languageOptions.forEach((button) => {
    button.addEventListener('click', () => {
      const selected = button.dataset.lang;
      applyLanguage(selected);
      languageOptions.forEach((node) => {
        node.classList.toggle('is-active', node === button);
      });
      closeLanguageMenu();
    });
  });

  if (copyCAButton) {
    copyCAButton.addEventListener('click', async () => {
      const caValue = copyCAButton.dataset.ca || 'TBA';
      try {
        await navigator.clipboard.writeText(caValue);
        showCopyStatus('copied');
      } catch {
        showCopyStatus('copyFailed');
      }
    });
  }

  if (leafLayer) {
    const createLeaf = () => {
      const leaf = document.createElement('span');
      leaf.textContent = '🍃';
      leaf.style.position = 'absolute';
      leaf.style.left = `${Math.random() * 100}vw`;
      leaf.style.top = '-8vh';
      leaf.style.fontSize = `${0.7 + Math.random() * 1.1}rem`;
      leaf.style.opacity = `${0.15 + Math.random() * 0.45}`;
      leaf.style.filter =
        'sepia(0.8) saturate(1.5) hue-rotate(-18deg) brightness(0.95) drop-shadow(0 2px 3px rgba(0,0,0,0.2))';
      leaf.style.animation = `leafFloat ${11 + Math.random() * 7}s linear forwards`;
      leafLayer.appendChild(leaf);
      leaf.addEventListener('animationend', () => leaf.remove());
    };

    if (!document.querySelector('#leaf-keyframes')) {
      const style = document.createElement('style');
      style.id = 'leaf-keyframes';
      style.textContent = `
        @keyframes leafFloat {
          0% { transform: translate3d(0,0,0) rotate(0deg); }
          100% { transform: translate3d(${(Math.random() > 0.5 ? 1 : -1) * (20 + Math.random() * 60)}vw, 110vh, 0) rotate(${120 + Math.random() * 240}deg); }
        }
      `;
      document.head.appendChild(style);
    }

    window.setInterval(createLeaf, 900);
  }

  loadMessages();
  window.setInterval(loadMessages, 5000);
};

document.addEventListener('DOMContentLoaded', initApp);